"""
    字面值(各种书写方式)
"""
# 1. int类型
# --十进制：每位用十种状态计数，逢十进一，写法是0~9。
number01 = 10
# --二进制：每位用二种状态计数，逢二进一，写法是0~1。
# 0 1 10 11 100 101  110  111
number02 = 0b10
# --八进制：每位用八种状态计数，逢八进一，写法是0~7。
# 0 1 ~ 7 10 11 12 13
number03 = 0o10
print(number03)
# --十六进制：每位用十六种状态计数，逢十六进一，写法是0~9  a(10)~f(15)
# 0 1 ~ 9 a ~ f  30
number04 = 0x10
print(number04)

# 2. 浮点型
# -- 小数
number05 = 1.2
# -- 科学记数法
number06 = 12e-1
print(number05 == number06)
# 5e-05
print(0.00005)