"""
    通用操作
        + * 比较运算符 成员运算符
"""
# + 拼接
print("悟空" + "八戒")
# * 重复
print("悟空" * 2)
# ==  != 依次比较两个容器中元素,一但不同则返回比较结果。
print("唐僧" == "如来佛祖")

# 相同且连续
print("悟空" in "我是花果山水帘洞美猴王孙悟空")# True
print("美猴" in "我是花果山水帘洞美猴王孙悟空")# True
print("水洞" in "我是花果山水帘洞美猴王孙悟空")# True
print("空悟" in "我是花果山水帘洞美猴王孙悟空")# True
print("空悟" not in "我是花果山水帘洞美猴王孙悟空")# True

